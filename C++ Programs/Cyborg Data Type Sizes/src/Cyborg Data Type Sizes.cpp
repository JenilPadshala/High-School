#include <iostream>
using namespace std;

int main()
{
	cout << "Size of character:"<<sizeof(char)<<endl; // prints Size of character
	cout << "Size of integer:" <<sizeof(int)<<endl ; // prints Size of integer
	cout << "Size of float:" <<sizeof(float)<<endl ; // prints Size of float
	cout << "Size of double:" <<sizeof(double)<<endl ; // prints Size of double
	return 0;
}
